# criar classe chamada 'calculadora'

class Calculadora:

#criando metodo chamado soma:
    def soma(self, n1, n2):
        return n1 + n2
#criando metodo chamado Subtração:    
    def subtracao(self, n1, n2):
        return n1 - n2
#criando metodo chamado Multiplicação:    
    def multiplicacao(self, n1, n2):
        return n1 * n2
#criando metodo chamado Divisão:   
    def divisao(self, n1, n2):
        return n1 / n2
#criando metodo chamado Resto:    
    def resto(self, n1, n2):
        return n1 % n2
    
